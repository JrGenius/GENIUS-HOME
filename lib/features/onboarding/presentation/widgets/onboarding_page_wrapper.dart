import 'package:flutter/material.dart';

/// Wraps each onboarding page content with a fade+slide entrance animation
class OnboardingPageWrapper extends StatefulWidget {
  const OnboardingPageWrapper({super.key, required this.child});
  final Widget child;

  @override
  State<OnboardingPageWrapper> createState() => _OnboardingPageWrapperState();
}

class _OnboardingPageWrapperState extends State<OnboardingPageWrapper>
    with SingleTickerProviderStateMixin {
  late final AnimationController _ctrl;
  late final Animation<double> _opacity;
  late final Animation<Offset> _slide;

  @override
  void initState() {
    super.initState();
    _ctrl = AnimationController(
      vsync: this,
      duration: const Duration(milliseconds: 500),
    );
    _opacity = CurvedAnimation(parent: _ctrl, curve: Curves.easeOut);
    _slide = Tween<Offset>(
      begin: const Offset(0, 0.05),
      end: Offset.zero,
    ).animate(CurvedAnimation(parent: _ctrl, curve: Curves.easeOut));
    _ctrl.forward();
  }

  @override
  void dispose() {
    _ctrl.dispose();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    return FadeTransition(
      opacity: _opacity,
      child: SlideTransition(position: _slide, child: widget.child),
    );
  }
}
